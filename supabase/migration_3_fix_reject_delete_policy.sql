-- Migration 3: Admin Panel-এর "প্রত্যাখ্যান (Reject)" বাটন ফিক্স
-- ============================================================
-- সমস্যা: profiles টেবিলে DELETE-এর জন্য কোনো RLS policy ছিল না,
-- তাই admin অনুমোদন-অপেক্ষমান প্রোফাইল রিজেক্ট (delete) করতে
-- চাইলে RLS চুপচাপ ডিলিট আটকে দিত (কোনো error ছাড়াই, ০ row affected)।
--
-- এই ফাইলটা Supabase Dashboard → SQL Editor-এ গিয়ে একবার Run করুন।
-- ============================================================

create policy "profiles_delete_admin"
  on public.profiles for delete
  to authenticated
  using (public.is_admin_user());

